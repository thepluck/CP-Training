int n,q;
alignas(32) int arr[200005],brr[200005],crr[200005];
 
int Min(int x, int y) {
  return y^((x^y) & -(x<y));
}
 
signed main(){
 
    cin>>n>>q;
    for (int i = 1; i <= n; i++) read(arr[i]);
    for (int i = 1; i <= n; i++) read(brr[i]);
    for (int i = 1; i < n; i++) read(crr[i]);
 
    while (q--){
        int p;
        read(p);
        read(arr[p], brr[p], crr[p]);
 
        long long ans=0;
        int tmp = 0;
        for (int i = 1; i <= n; i++) {
            if ((tmp += arr[i]) < brr[i]) {
              ans += tmp; tmp = 0;
            } else {
              ans += brr[i];
              tmp = Min(crr[i], tmp - brr[i]);
            }
        }
        print(ans);
    }
}
